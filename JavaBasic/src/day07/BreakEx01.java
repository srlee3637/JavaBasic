package day07;

public class BreakEx01 {

	public static void main(String[] args) {
		
		
		int i = 1;
		while(i <= 10) {

			System.out.println(i);
			
			if(i == 5) break;
			
			i++;
		}
		
		
		
		for(i = 1; i <= 10; i++) {
			
			System.out.println(i);
			if(i == 5) break;
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
